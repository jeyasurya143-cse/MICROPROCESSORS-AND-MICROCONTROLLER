// Double 7 Segment Display in Decreasing order

                OUTPUT 2500AD
.ORG 2000H
DATA SEGMENT
LIST DB 71H,79H,5EH,39H,7CH,77H,6FH,7FH,07H,7DH,6DH,66H,4FH,5BH,06H,03H
LIST1 DB 71H,79H,5EH,39H,7CH,77H,6FH,7FH,07H,7DH,6DH,66H,4FH,5BH,06H,03H
COUNT DB 10H
DATA ENDS
CODE SEGMENT
        ASSUME CS:CODE, DS:DATA
        MOV AX, DATA
        MOV DS, AX
        MOV AL,80H
        MOV DX,FFE6H
        OUT DX,AL
REPEAT: LEA SI, LIST
        LEA DI, LIST1
        MOV CL, COUNT
L1:     MOV AL, [SI]
        MOV DX, FFE0H
        OUT DX, AL
        MOV AL,[DI]
        MOV DX, FFE2H
        OUT DX, AL
        CALL DELAY
        INC SI
        INC DI
        DEC CL
        JNZ L1
        JMP REPEAT
DELAY:  MOV AH, 15H
L3:     MOV BX, AAAAH
L2:     DEC BX
        JNZ L2
        DEC AH
        JNZ L3
        RET
CODE ENDS
END
