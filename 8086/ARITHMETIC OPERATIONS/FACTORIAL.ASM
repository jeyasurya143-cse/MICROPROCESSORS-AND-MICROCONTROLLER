//FACTORIAL OF A GIVEN NUMBER

        OUTPUT 2500AD
.ORG 2000H
DATA SEGMENT
NUM DW 0005H
RESULT DW 0001H
DATA ENDS
CODE SEGMENT
        ASSUME CS:CODE,DS:DATA
START:
        MOV AX,DATA
        MOV DS,AX
        MOV AX,0001H
        MOV CX,NUM
L1:
        MUL CX
        LOOP L1
        MOV RESULT,AX
        HLT
CODE ENDS
END
